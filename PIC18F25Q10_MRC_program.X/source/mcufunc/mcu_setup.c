/**************************************************************/
/*  Function:                                                 */
/*  レジスタ初期設定                                            */
/*                                                            */
/**************************************************************/
void func_mset_g_init( void )
{
    func_mset_s_option_reg_setup();     /* オプションレジスタ設定 */
    func_mset_s_oscillator_setup();     /* マイコンクロック設定  */
    func_mset_s_int_setup();            /* 割り込み設定         */
    func_mset_s_port_setup();           /* ポート設定           */
    func_mset_s_adc_setup();            /* AD変換設定           */
    func_mset_s_timer_setup();          /* タイマ設定           */
}


/**************************************************************/
/*  Function:                                                 */
/*  プログラム開始直前の設定                                    */
/*                                                            */
/**************************************************************/
void func_mset_g_mcu_start_condition( void )
{
    INTCON |= 0x80U;     /* 全体割り込み 許可 */
    ADCON0 |= 0x00U;     /* AD変換器 動作許可 */
}



/**************************************************************/
/*  Function:                                                 */
/*  オプションレジスタ初期設定                                   */
/*  クロックより先に設定したほうがよさそう                        */
/**************************************************************/
static void func_mset_s_option_reg_setup( void )
{
    /*=================================================================*/
                            /*  +------------------------- 7 : nWPUEN  */
                            /*  | +----------------------- 6 : INTEG   */
                            /*  | | +--------------------- 5 : TMR0CS  */  /* タイマ0:内部クロックを使用する */
                            /*  | | | +------------------- 4 : TMR0SE  */  /* 内部クロック使用するので、設定は意味なしだぉ */ 
                            /*  | | | |                                */
                            /*  | | | |     +------------- 3 : PSA     */  /* タイマ0のクロックにはプリスケーラ適用する */
                            /*  | | | |     | +----------- 2 : PS<2>   */  /* タイマ0のクロックは 1:256 を使用する */
                            /*  | | | |     | | +--------- 1 : PS<1>   */
                            /*  | | | |     | | | + ------ 0 : PS<0>   */
    OPTION_REG =  0x06U;    /*  0 0 0 0  -  0 1 1 0                    */
    /*-----------------------------------------------------------------*/
    /* memo                                                            */
    /*                                                                 */
    /*=================================================================*/

    /* タイマ0 設定メモ */
    /* クロック=16MHz */
    /* 16MHz/128 = 125,000 Hz */
    /* 所望のメインタスク周期：10ms = 100Hz */
    /* !!タイマ0のオーバーフロー周期を1kHzに設定 */
    /* 125,000Hz / 125 = 1,000 */
    /* あとはソフトウェアで分周。。。するか。 */
}



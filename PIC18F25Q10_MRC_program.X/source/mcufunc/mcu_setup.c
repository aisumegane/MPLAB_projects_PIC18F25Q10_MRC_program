    T2HLT = 0xA0U;          /*  1 0 1 0  -  0 0 0 0                    */
    /*-----------------------------------------------------------------*/
    /* memo                                                            */
    /*                                                                 */
    /*=================================================================*/

    /* タイマ クロック選択 */
    /*=================================================================*/

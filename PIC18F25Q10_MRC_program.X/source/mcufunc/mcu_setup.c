/**************************************************************/
/*  Function:                                                 */
/*  プログラム開始直前の設定                                    */
/*                                                            */
/**************************************************************/
void func_mset_g_mcu_start_condition( void )
{
    INTCON |= 0xC0U;     /* 全体割り込み(高優先度&低優先度) 許可 */
    ADCON0 |= 0x00U;     /* AD変換器 動作許可 */

    /* タイマスタート */
    /* PIC18F25Q10はレジスタ数多いのでそこそこ設定に時間かかる */
    /* 初期化中に割り込みが入るとプログラム開始しなくなるので、 */
    /* この関数を実行するタイミングですべてのタイマ動作許可を行う */
    T0EN = SET;             /* Timer0 カウントスタート */
    /*TMR5ON = SET;*/       /* Timer5 カウントスタートしない 各処理で設定 */
    TMR1ON = SET;           /* Timer1 モータ回転数検出 開始 */
    TMR3ON = SET;           /* Timer1 1次ギヤ回転数検出 開始 */

    TMR2ON = SET;           /* クラッチ用サーボ PWMタイマ 開始 */
    DAC1OE2 = SET;          /* DAC出力 許可 */
}


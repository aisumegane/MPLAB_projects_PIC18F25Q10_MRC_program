                            /*  | | | |     | +----------- 2 : TMRxON     */    /* タイマ2動作 許可 */
                            /*  | | | |     | | +--------- 1 : TxCKPS<1>  */    /* タイマ2 プリスケーラ：11 -> 1/64 */
                            /*  | | | |     | | | + ------ 0 : TxCKPS<0>  */
    T6CON = 0x07U;          /*  0 0 0 0  -  0 1 1 1                       */
    /*--------------------------------------------------------------------*/
    /* memo                                                               */
    /* クロック:16MHz                                                      */
    /* ペリフェラルクロック：Fosc/4 = 4MHz                                  */
    /* プリスケーラ：1/64 -> 62,500Hz                                      */
    /* 一致カウント数(キャリア周期カウントPRx)設定：255 -> 12,500/(255+1) = 244.14...Hz -> 4.096...ms */
    /*====================================================================*/


    /* タイマ4 一致割り込み発生数値 */
    /* PWMのキャリア周期になる */
    /*=================================================================*/
                            /*  +------------------------- 7 :         */
                            /*  | +----------------------- 6 :         */
                            /*  | | +--------------------- 5 :         */
                            /*  | | | +------------------- 4 :         */
